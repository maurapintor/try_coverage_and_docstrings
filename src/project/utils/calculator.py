"""Calculator functions."""


def calc_sum(a: float, b: float) -> float:
    """
    Sum two elements.

    Parameters
    ----------
    a : float
        A number.
    b : float
        Another number.

    Returns
    -------
    float
        The sum of the two inputs.
    """
    return a + b


def calc_mul(a: float, b: float) -> float:
    """
    Multiply two elements.

    Parameters
    ----------
    a : number
        A number.
    b : number
        Another number.

    Returns
    -------
    float
        The product of the two inputs.
    """
    return a * b
