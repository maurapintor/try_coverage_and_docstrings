"""Calculator functions."""


def calc_sum(a, b):
    """
    Sum two elements.

    Parameters
    ----------
    a : number
        A number.
    b : number
        Another number.

    Returns
    -------
    number
        The sum of the two inputs.
    """
    return a + b


def calc_mul(a, b):
    """
    Multiply two elements.

    Parameters
    ----------
    a : number
        A number.
    b : number
        Another number.

    Returns
    -------
    number
        The product of the two inputs.
    """
    return a * b
